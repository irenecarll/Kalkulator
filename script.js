function tambah(id, input1, input2) {
    document.getElementById(id).value = Number(input1) + Number(input2);

}

function kurang(id, input1, input2) {
    document.getElementById(id).value = Number(input1) - Number(input2);
}

function kali(id, input1, input2) {
    document.getElementById(id).value = Number(input1) * Number(input2);
}

function bagi(id, input1, input2) {
    document.getElementById(id).value = Number(input1) / Number(input2);
}

